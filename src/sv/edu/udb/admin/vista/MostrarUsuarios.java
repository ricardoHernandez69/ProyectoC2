    private void llenarTabla() {
        txtTipo.setEditable(false);
        txtArea.setEditable(false);
        txtCargo.setEditable(false);
        txtUsuario.setEditable(false);
        ModeloUsuario mUsuario=new ModeloUsuario();
        ArrayList<BeansDatosEmpleados> arreglo=mUsuario.mostrarEmpleados();
        modelo1.setNumRows(0);
        for(BeansDatosEmpleados user:arreglo){
             Object[] newRow={user.getIdEmpleado(),user.getNombre(),user.getApellido(),user.getDui(),user.getFecha(),user.getUsuario(),user.getTipo(),user.getArea(),user.getCargo()};
             modelo1.addRow(newRow);
        }
    }
