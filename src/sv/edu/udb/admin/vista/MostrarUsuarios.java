    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        fila=jTable1.rowAtPoint(evt.getPoint());
        int columna=jTable1.columnAtPoint(evt.getPoint());
        if((fila>-1)&&(columna>-1)){
            id=Integer.parseInt(modelo1.getValueAt(fila, 0).toString());
            txtNombre.setText(modelo1.getValueAt(fila, 1).toString());
            txtApellido.setText(modelo1.getValueAt(fila, 2).toString());
            txtDui.setText(modelo1.getValueAt(fila, 3).toString());
            txtFecha.setText(modelo1.getValueAt(fila, 4).toString());
            txtUsuario.setText(modelo1.getValueAt(fila, 5).toString());
            txtTipo.setText(modelo1.getValueAt(fila, 6).toString());
            txtArea.setText(modelo1.getValueAt(fila, 7).toString());
            txtCargo.setText(modelo1.getValueAt(fila, 8).toString());
        }
        
    }//GEN-LAST:event_jTable1MouseClicked

    private void btnModificaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificaActionPerformed
        // TODO add your handling code here:
        String nombre=txtNombre.getText();
        String apellido=txtApellido.getText();
        String dui=txtDui.getText();
        String fecha=txtFecha.getText();
        ModeloUsuario mUsu=new ModeloUsuario();
        if(mUsu.updateUsuario(id, nombre, apellido, dui, fecha)==true){
            llenarTabla();
            JOptionPane.showMessageDialog(this, "El usuario se ha modificado con Exito");
           
        }else{
            JOptionPane.showMessageDialog(this, "No se pudo modificar el usuario","Error",JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnModificaActionPerformed

    private void txtFechaKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtFechaKeyTyped
        // TODO add your handling code here:
        if(!Character.isDigit(evt.getKeyChar())){
            tamaño=txtFecha.getText().length()-10;  
            Toolkit.getDefaultToolkit().beep();
            evt.consume(); 
        }else{
            tamaño=txtFecha.getText().length(); 
        }
        if(tamaño==2){
            txtFecha.setText(txtFecha.getText()+"-");
        }
        if(tamaño==5){
            txtFecha.setText(txtFecha.getText()+"-");
        }else if(tamaño>9){
            Toolkit.getDefaultToolkit().beep();
            evt.consume(); 
        }else{
            txtFecha.setText(txtFecha.getText());
        }
    }//GEN-LAST:event_txtFechaKeyTyped

    private void txtDuiKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDuiKeyTyped
        // TODO add your handling code here:
        if(!Character.isDigit(evt.getKeyChar())){
            tamaño=txtDui.getText().length()-2;  
            Toolkit.getDefaultToolkit().beep();
            evt.consume(); 
        }else{
            tamaño=txtDui.getText().length(); 
        }
        if(tamaño==8){
            txtDui.setText(txtDui.getText()+"-");
        }else if(tamaño>9){
            Toolkit.getDefaultToolkit().beep();
            evt.consume(); 
        }else{
            txtDui.setText(txtDui.getText());
        }
    }//GEN-LAST:event_txtDuiKeyTyped

